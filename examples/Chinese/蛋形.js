蛋形 = (x, y, 尺寸, 斜率) => {
  抬起笔刷();
  改变位置(x, y);
  放下笔刷();
  改变方向(270 + 斜率);
  改变颜色为(4); // 红色
  向右弯曲(尺寸, 180);
  改变颜色为(1); // 蓝色
  向右弯曲(2 * 尺寸, 45);
  改变颜色为("ఆకుపచ్చ");
  向右弯曲(0.586 * 尺寸, 90);
  改变颜色为(1); // 蓝色
  向右弯曲(2 * 尺寸, 45);
};

演示 = () => {
  初始状态();
  隐藏笔刷();
  蛋形(90, 90, 40, 0);
  蛋形(0, 0, 90, 45);
};

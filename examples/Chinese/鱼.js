平方根 = Math.sqrt;

演示 = () => {
  初始状态();
  隐藏笔刷();

  鱼 = (尺寸) => {
    圆形(尺寸);
    向右转(90);

    抬起笔刷();
    向前移动(尺寸);
    放下笔刷();

    显示笔刷();

    向左转(45);
    向前移动(2 * 尺寸);
    向右转(90 + 45);

    向前移动(平方根(2 * 尺寸 * 2 * 尺寸 * 2));

    向右转(90 + 45);
    向前移动(2 * 尺寸);
    向左转(45);

    // 隐藏笔刷();

    抬起笔刷();
    向前移动(尺寸);
    放下笔刷();

    向右转(90);
  };

  颜色_鱼 = (颜色编号) => {
    改变颜色为(颜色编号);
    鱼(40 + 颜色编号 * 1);
  };
  计数并重复(16, (颜色) => 颜色_鱼(颜色));
};
